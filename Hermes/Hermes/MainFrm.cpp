BOOL CMainFrame::OnCmdMsg(UINT nID, int nCode, void * pExtra, AFX_CMDHANDLERINFO * pHandlerInfo)
{
	//	TRACE(_T("BOOL CMainFrame::OnCmdMsg(UINT nID, int nCode, void * pExtra, AFX_CMDHANDLERINFO * pHandlerInfo) ----->\n"));

	//route cmd first to registered dockable pane
	POSITION pos = m_regCmdMsg.GetHeadPosition();
	while (pos)
	{
		CBasePane* pane = m_regCmdMsg.GetAt(pos);
		if (pane->IsVisible() && pane->OnCmdMsg(nID, nCode, pExtra, pHandlerInfo))
		{
			TRACE(_T("BOOL CMainFrame::OnCmdMsg(UINT nID, int nCode, void * pExtra, AFX_CMDHANDLERINFO * pHandlerInfo) <-----\n"));
			return TRUE;
		}
		m_regCmdMsg.GetNext(pos);
	}

	//	TRACE(_T("BOOL CMainFrame::OnCmdMsg(UINT nID, int nCode, void * pExtra, AFX_CMDHANDLERINFO * pHandlerInfo) <-----\n"));

	return CMDIFrameWndEx::OnCmdMsg(nID, nCode, pExtra, pHandlerInfo);
}

