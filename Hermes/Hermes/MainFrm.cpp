void CMainFrame::RegCmdMsg(CBasePane* pane)
{
	TRACE(_T("void CMainFrame::RegCmdMsg(CBasePane* pane) ----->\n"));

	m_regCmdMsg.AddHead(pane);

	TRACE(_T("void CMainFrame::RegCmdMsg(CBasePane* pane) <-----\n"));
}


/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//	[SBROTHY]: 
//
//		Manually unregister CDockablePane with the message routing system.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
BOOL CMainFrame::UnregCmdMsg(CBasePane* pane)
{
	TRACE(_T("BOOL CMainFrame::UnregCmdMsg(CBasePane* pane) ----->\n"));

	POSITION p = m_regCmdMsg.Find(pane, 0);
	if (NULL == p)
	{
		TRACE(_T("BOOL CMainFrame::UnregCmdMsg(CBasePane* pane) <-----\n"));
		return FALSE;
	}

	m_regCmdMsg.RemoveAt(p);

	TRACE(_T("BOOL CMainFrame::UnregCmdMsg(CBasePane* pane) <-----\n"));

	return TRUE;
}


